package com.wensen.day.two;

public class Code {

	private Code next;
	private int position;
	private int value;
	
	public Code getNext() {
		return next;
	}
	public void setNext(Code next) {
		this.next = next;
	}
	public int getPosition() {
		return position;
	}
	public void setPosition(int position) {
		this.position = position;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	
}
